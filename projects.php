<script>
    // jQuery for filter navigation
    $(document).ready(function () {
        // Get all filter items
        var filterItems = $('.cbp-filter-item');
        
        // Add click event listeners to filter items
        filterItems.click(function () {
            // Remove the 'active' class from all filter items
            filterItems.removeClass('cbp-filter-item-active');

            // Add the 'active' class to the clicked filter item
            $(this).addClass('cbp-filter-item-active');

            // Get the data-filter value of the clicked filter item
            var filterValue = $(this).data('filter');

            // Get all portfolio items
            var portfolioItems = $('.cbp-item');

            // Loop through portfolio items and show/hide them based on the selected filter classes
            portfolioItems.each(function () {
                // Check if the portfolio item has any of the classes specified in filterValue
                var classes = filterValue.split(' ');
                var showItem = false;
                for (var i = 0; i < classes.length; i++) {
                    if (classes[i] === '*' || $(this).is(classes[i])) {
                        showItem = true;
                        break;
                    }
                }

                if (showItem) {
                    $(this).removeClass('hidden');
                } else {
                    $(this).addClass('hidden');
                }
            });
        });
    });
</script>

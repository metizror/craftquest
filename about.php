                    <h2 class="title">Principal</h2>
                    <p>Urvi Sheth is passionate about mixing state of the art technology, regional materials and crafts. Her research and practice has a global design approach and regional/local in execution. Her work aims to define contemporary construction-craft by generating designs through computational tools and structural concepts. These designs are meticulously constructed by digital fabrication and craftsmanship in India.</p>
                    <p>She has mentored innovation competitions, facilitated workshops and created learning experiences on invitation by foreign nations. Her work is published in reputed architectural magazines like Domus India and international organisations like CAADRIA and eCAADe.</p>
                    <p>She has been appointed as Workshop Head with CEPT University from 15th November 2014. As Workshop and Exhibition Head, she is involved in deepening “the culture of making” at CEPT University. She is elected as the Secretary of CAADRIA (The association of Computer-Aided Design Research in Asia) this year (2020). </p>
